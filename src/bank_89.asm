;;; $AB82: Load FX header ;;;
Load_FX_Header:
    PHP : PHB
    REP #$30
    PEA.W FXHeaders>>8 : PLB : PLB
    LDX.W FXPointer : BNE +
    PLB : PLP
    RTL

+   LDA.W $0000,X
    LDY.W #$0000

  .loopDoor:
    LDA.W $0000,X : BEQ .load
    CMP.W #$FFFF : BNE +
    PLB : PLP
    RTL

+   CMP.W DoorPointer : BEQ .load
    TXA : CLC : ADC.W #$0010 : TAX
    TYA : CLC : ADC.W #$0010 : TAY
    BRA .loopDoor

  .load:
    STY.W FX_CurrentEntryOffset
    STX.W FX_CurrentEntryPointer
    LDX.W FX_CurrentEntryPointer ; >.<
    LDA.W $0002,X : STA.W FX_BaseYPosition
    LDA.W $0004,X : STA.W FX_TargetYPosition
    LDA.W $0006,X : STA.W FX_YSubVelocity
    LDA.W $0008,X : AND.W #$00FF : STA.W FX_Timer
    LDA.W $000A,X : AND.W #$00FF : STA.W LayerBlending_DefaultConfig
    LDA.W $000B,X : AND.W #$00FF : STA.W LayerBlending_Layer3Config
    LDA.W $000C,X : AND.W #$00FF : STA.W FX_LiquidOptions
    LDA.W $000F,X : AND.W #$00FF : BEQ .blend0
    TAX
    LDA.L PaletteBlends_Bank89_Color1,X : STA.L TargetPalettes_BGP1+$12
    LDA.L PaletteBlends_Bank89_Color2,X : STA.L TargetPalettes_BGP1+$14
    LDA.L PaletteBlends_Bank89_Color3,X : STA.L TargetPalettes_BGP1+$16
    BRA +

  .blend0:
    LDA.W #$0000 : STA.L TargetPalettes_BGP1+$16

+   LDX.W FX_CurrentEntryPointer
    LDA.W $0009,X : AND.W #$00FF : STA.W FX_Type : BEQ +
    TAY
    LDA.W FXType_Tilemap_Pointers,Y : STA.W FX_TilemapPointer

+   LDX.W FX_CurrentEntryPointer
    LDA.W $0009,X : AND.W #$00FF : BEQ .JMLreturn
    TAY
    LDA.W #$8800 : STA.B DP_Temp13
    LDA.W FXType_Function_Pointers,Y : STA.B DP_Temp12
    PHK : PEA .JMLreturn-1
    JML.W [DP_Temp12]


    LDX.W FX_CurrentEntryPointer
    LDA.W $000D,X : AND.W #$00FF : BEQ .paletteFXEnd
    STA.W FX_CurrentPaletteAnimatedTilesBitset
    LDA.W AreaIndex : ASL : TAY
    LDA.W AreaSpecific_PaletteFXObjectList_Pointers,Y : STA.B DP_CurrentAreaFXAnimTilesObjTable
    LDY.W #$0000

  .loopPaletteFX:
